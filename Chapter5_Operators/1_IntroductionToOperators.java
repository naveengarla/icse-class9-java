package Chapter5_Operators;
// 1_IntroductionToOperators.java
// Overview of operators in Java with real-life analogies and types

class IntroductionToOperators {
    public static void main(String[] args) {
        // What are operators?
        System.out.println("Operators are symbols that perform operations on variables and values.");
        System.out.println("Example: +, -, *, /, %, ==, &&, ||, =, ++, --, new, .");

        // Real-life analogy
        System.out.println("Just like a calculator uses + for addition, Java uses + to add numbers.");
        System.out.println("Operators help us compute, compare, assign, and manipulate data.");

        // Types of operators in Java
        System.out.println("Types of Operators in Java:");
        System.out.println("1. Arithmetic Operators (+, -, *, /, %)");
        System.out.println("2. Relational Operators (==, !=, >, <, >=, <=)");
        System.out.println("3. Logical Operators (&&, ||, !)");
        System.out.println("4. Assignment Operators (=, +=, -=, *=, /=)");
        System.out.println("5. Increment/Decrement Operators (++/--)");
        System.out.println("6. Special Operators (new, .)");
    }
}
