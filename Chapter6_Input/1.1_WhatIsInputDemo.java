package Chapter6_Input;
// 1.1_WhatIsInputDemo.java
// Introduction: Understanding what input means in programming

class WhatIsInputDemo {
    public static void main(String[] args) {
        System.out.println("=== WHAT IS INPUT? ===");
        System.out.println("Understanding how programs get information from users\n");

        // What is input?
        System.out.println("--- WHAT IS INPUT? ---");
        System.out.println("Input = Information that you give to a program");
        System.out.println("Just like when someone asks you a question, you give an answer!");
        System.out.println();

        // Real-life examples
        System.out.println("--- REAL-LIFE EXAMPLES ---");
        System.out.println("🏧 ATM asks: 'Enter your PIN' → You type: 1234");
        System.out.println("🎮 Game asks: 'Enter your name' → You type: Alex");
        System.out.println("📱 Calculator asks: 'Enter first number' → You type: 25");
        System.out.println("🛒 Shopping app asks: 'Enter quantity' → You type: 3");
        System.out.println();

        // Why do programs need input?
        System.out.println("--- WHY DO PROGRAMS NEED INPUT? ---");
        System.out.println("1. To personalize the experience");
        System.out.println("   Example: 'Hello Alex!' instead of 'Hello User!'");
        System.out.println();
        System.out.println("2. To perform calculations with your numbers");
        System.out.println("   Example: Calculate your age, grade, shopping total");
        System.out.println();
        System.out.println("3. To make decisions based on your choices");
        System.out.println("   Example: Show different content based on your age");
        System.out.println();
        System.out.println("4. To save your preferences");
        System.out.println("   Example: Remember your favorite color, subject");
        System.out.println();

        // Types of input
        System.out.println("--- TYPES OF INPUT ---");
        System.out.println("📝 Text input:");
        System.out.println("   • Your name: 'Rahul'");
        System.out.println("   • Your city: 'Mumbai'");
        System.out.println("   • Your favorite subject: 'Mathematics'");
        System.out.println();
        System.out.println("🔢 Number input:");
        System.out.println("   • Your age: 15");
        System.out.println("   • Your height: 5.6");
        System.out.println("   • Number of books: 25");
        System.out.println();
        System.out.println("✅ Yes/No input:");
        System.out.println("   • Do you like pizza? true/false");
        System.out.println("   • Are you a student? yes/no");
        System.out.println();

        // Input vs Output
        System.out.println("--- INPUT VS OUTPUT ---");
        System.out.println("INPUT  → What you give TO the program");
        System.out.println("OUTPUT → What the program gives TO you");
        System.out.println();
        System.out.println("Example conversation:");
        System.out.println("  Program OUTPUT: 'What is your name?'");
        System.out.println("  Your INPUT: 'Priya'");
        System.out.println("  Program OUTPUT: 'Hello Priya, nice to meet you!'");
        System.out.println();

        // What we'll learn
        System.out.println("--- WHAT WE'LL LEARN ---");
        System.out.println("In this chapter, you'll learn how to:");
        System.out.println("✓ Ask users for information");
        System.out.println("✓ Read different types of input (text, numbers)");
        System.out.println("✓ Use the input in calculations");
        System.out.println("✓ Create interactive programs");
        System.out.println("✓ Handle common input mistakes");
        System.out.println();
        System.out.println("🚀 Let's start making our programs interactive!");
    }
}
