package Chapter7_MathLibrary;
// 1_UsingMathClass.java
// Introduction to using the Math class in Java

class UsingMathClass {
    public static void main(String[] args) {
        System.out.println("The Math class provides mathematical functions and constants.");
        System.out.println("Examples: Math.abs(), Math.sqrt(), Math.pow(), Math.random(), Math.sin(), Math.PI");
        System.out.println("Math.PI = " + Math.PI);
        System.out.println("Math.E = " + Math.E);
    }
}
